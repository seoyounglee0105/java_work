package ch03;

public class Coffee {

	private int id;
	private String name;
	private double price;
	
	public Coffee(int id, String name, double price) {
		this.id = id;
		this.name = name;
		this.price = price;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public double getPrice() {
		return price;
	}
	
	
	
	
}
