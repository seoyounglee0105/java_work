package ch01;

public class DataType3 {
	
	public static void main(String[] args) {
		// 실수형
		// 1. float : 4byte
		// 2. double : 8byte
		
//		float a = 0.5;  // 오류 (Type mismatch)
		float a = 0.5F;  // 접미사를 붙이면 정상 작동
		float b = 12.9F;
		
		// 실수형의 R value 기본 연산 단위 : double (더 정확한 실수형이라서)
		
		double c = 0.12345;
		double d = 0.55;
		
	} // end of main
	
} // end of class
